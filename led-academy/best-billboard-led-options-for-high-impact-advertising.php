                <p><strong>Size Range:</strong> 1500 × 1000 mm up to 6000 × 3000 mm (1.5 m × 1 m to 6 m × 3 m)</p>

                <p><strong>Best for:</strong></p>

                <ul>
                    <li>Local roadside promotions</li>
                    <li>Retail outlets and shopping areas</li>
                    <li>Municipal announcements and campaigns</li>
                </ul>

                <p><strong>Why it works:</strong></p>

                <ul>
