                <p>Not all messages require a skyscraper. Small and mid-size displays are ideal for more focused, local applications.</p>

                <p><strong>Size Range:</strong> 1000 × 500 mm up to 4000 × 3000 mm (1 m × 0.5 m to 4 m × 3 m)</p>

                <p><strong>Best for:</strong></p>

                <ul>
                    <li>Storefronts</li>
                    <li>Community centers</li>
                    <li>Gas stations, restaurants, and car washes</li>
                    <li>Local events or sponsor advertising</li>
                </ul>

                <p><strong>Why Businesses Use Them:</strong></p>

                <ul>
                    <li>Less expensive and easier to install</li>
                    <li>Suitable for shorter viewing distances</li>
                    <li>Fixed-size options, but flexible placement</li>
                    <li>Central digital updating with no reprinting required</li>
                    <li>Indoor or outdoor capability</li>
                </ul>

                <p><strong>Average Cost:</strong></p>

                <p>$5,000 to $30,000 based on size, pitch, and frame arrangement.</p>

                <p><strong>Other Use Case:</strong></p>

                <p>Franchise companies frequently use mid-sized displays at multiple locations. This provides them with uniform branding and promotions that can be centrally updated.</p>

