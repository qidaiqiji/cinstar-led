                <div id="chapter2" class="chapter-title">
                    <h3>Chapter 2: What is 480P, 720P, 1080P, 2K and 4K</h3>
                </div>
                <div class="img-box">
                    <img src="../images/products/Fine-Pitch/fine-pitch-2.jpg"/>
                </div>
                <p>
                As the preferable visual displaying system, LED display is adopted to display high definition or ultra-high-definition content. Referring to the resolution, you should need to figure out what is 480P, 720P, 1080P, 2K and 4K. 480P is the shorthand name for a family of video display resolutions with a vertical resolution of 480 pixels. It is usually used for DVDs and many early Plasma televisions. The Standard one has always been a 4:3 aspect ratio with a 640 × 480 pixels resolution. 
                </p>
                <p>
                720P is the standard high-definition (HD) display resolution of 1280x720 pixels, at a 16:9 aspect ratio. It is the most common resolution for any major HDTV broadcasting standards.
                </p>
                <p>
                1080P is also known as Full HD or FHD. It has a display resolution of 1920x1080 pixels and could show more details than 720P. This format resolution is usually adopted on a widescreen with an aspect ratio of 16:9. 2K is a pretty joint resolution that indoor LED displays frequently require to display. It stands for the resolution of 2048x1080. Even if it has the same pixels at vertical and very close pixels at horizontal as 1080P, they are generally defined as two different resolution formats.
                </p>
                <p>
                4k, also known as ultra-high definition (UHD). In television and consumer media, 4K refers to the display resolution of 3840x2160, the dominant 4K standard. But it relates to a display resolution of 4096x2160 in the movie projection industry. At present, 4K television occupies a significant market share of the fine pitch LED. Nobody refuses a display capable of showing more detailed and clearer video content. 
                </p>
                <p>
                When looking for an indoor LED screen, it is important to figure out its resolution. Take 4K resolution; for example, the lower the pixel pitch, the smaller the screen dimension required. And the cost of different pixel pitches varies a lot; after comparison, you will find out what pixel pitch and size are close to your budget. Besides, the viewing distance and visual experience (image quality on the screen) should also be considered.
                </p>
