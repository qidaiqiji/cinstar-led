                <p>Step 2: Wet cleaning. Using water spray and steam humidification to spray the LED display screen masks and use the soft brush on the vacuum cleaner to sweep the lamp tube mask to clean the dirt.</p>
                <p>Step 3: Drying. Using a vacuum cleaner to absorb the water droplets and water marks left by wet cleaning to ensure that the display mask is clean and dust-free.</p>
                <p>Cleaning after installation for a certain period of time is needed to be performed to remove the accumulated dust and impurities. It is necessary to first consider purchasing a cleaning kit with premium quality. The cost of cleaning solution generally includes electrolyte, high-purity distilled water and antistatic liquid etc., which can effectively clean the dust and other stains on the LED screen. It should be noted that you must not spray water directly to the LED screen, but spray a little cleaning liquid on the cleaning cloth and then gently wipe in the same direction. It is important that the power should be shut down before cleaning to make sure the safety.</p>
