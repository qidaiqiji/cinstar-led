                <p class="desc-title">
                    <b>A balance between sales and price</b>
                </p>
                <p>On the one hand, the market demand has been picking up slowly, and there is a lack of explosive    growth outlets. In general, the overall decline in the market for LED display products remains unchanged. Therefore, even if the current raw materials are rising rapidly, some screen companies and merchants are still adopting price reduction promotions in order to stimulate consumption and increase shipments, in order to take the lead in the market. According to industry insiders, as the cost of raw materials continues to rise, many manufacturers will still use price cuts as a means to make the first shots in the peak season market.</p>
                <p>On the other hand, since the second half of last year, the continuous surge in raw materials has brought many manufacturers a new issue of "quantity and price balance" in the terminal market operations. Under the situation that consumer demand has not changed, the cost of labor and raw materials has increased. Enterprise price increases, especially for small and medium-sized screen companies that are unable to absorb cost pressures on their own, price increases have become their inevitable choice. So, whether it is optimizing products and services or improving marketing strategies, how to make the market pay is the key they must think about.</p>
                <p class="desc-title">
                    <b>The continuous collision of tradition and novelty</b>
                </p>
                <p>
                As we all know, China’s LED display industry has always been channel winners, but it has   experienced the impact of the epidemic on offline physical channels (logistics, stores, etc.). Last year, many LED display companies actively explored online e-commerce platforms, live broadcasts and other new innovations. Channel form, online assistance, and offline only dual-line integration, for the industry now seems to be "a done deal". However, for all manufacturers, online and offline integration has never been the goal. It is the key point to promote the transformation of business models and the overall improvement of consumer experience by integrating the resources and strength of the two lines.</p>
                <p>The key to the rising cost and the downward market lies in how to “upgrade products to respond to rising costs, and to activate consumer demand to drive a positive market cycle”, and finally establish a new positive operating cycle in the ups and downs of the first-tier market.</p>
